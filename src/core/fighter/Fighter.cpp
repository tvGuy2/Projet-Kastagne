// Function refactored
bool Fighter::checkCollision(const BoundingBoxInfo& boxInfo) const {
    BoundingBox ownAtlas = m_currentSpriteRect->atlas;
    BoundingBox oppAtlas = m_opponent->getSpriteRect().atlas;

    int ownBoxPositionX, oppBoxPositionX;

    if(m_reversed) {
        ownBoxPositionX = m_position.x + (ownAtlas.width - boxInfo.ownBox.posx)*m_scale;
        oppBoxPositionX = m_opponent->m_position.x + boxInfo.oppBox.posx*m_scale;
    } else {
        ownBoxPositionX = m_position.x + boxInfo.ownBox.posx*m_scale;
        oppBoxPositionX = m_opponent->m_position.x + (oppAtlas.width - boxInfo.oppBox.posx)*m_scale;
    }

    bool horizontalCollision = (m_reversed ? 
        ownBoxPositionX - boxInfo.ownBox.width*m_scale < oppBoxPositionX + boxInfo.oppBox.width*m_scale :
        ownBoxPositionX + boxInfo.ownBox.width*m_scale > oppBoxPositionX - boxInfo.oppBox.width*m_scale);

    bool verticalCollision = m_position.y + ownAtlas.height*m_scale > m_opponent->m_position.y + boxInfo.oppBox.posy*m_scale &&
        m_position.y + boxInfo.ownBox.posy*m_scale < m_opponent->m_position.y + oppAtlas.height*m_scale;

    return horizontalCollision && verticalCollision;
}

bool Fighter::isCollidingWithOpponent() const {
    const BoundingBoxInfo boxInfo = {
        m_currentSpriteRect->collideBox,
        m_opponent->getSpriteRect().collideBox
    };
    return checkCollision(boxInfo);
}

bool Fighter::isHittingOpponent() const {
    const BoundingBoxInfo boxInfo = {
        m_currentSpriteRect->hitBox,
        m_opponent->getSpriteRect().hurtBox
    };
    return checkCollision(boxInfo);
}

bool Fighter::isBeingHurtByOpponent() const {
    const BoundingBoxInfo boxInfo = {
        m_currentSpriteRect->hurtBox,
        m_opponent->getSpriteRect().hitBox
    };
    if(m_opponent->getSpriteRect().hitBox.posx == 0 && m_opponent->getSpriteRect().hitBox.posy == 0) return false;
    return checkCollision(boxInfo);
}

