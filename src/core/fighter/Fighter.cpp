void Fighter::update(float deltaTime, bool playerId, std::string keycode) {
    m_position.x += m_velocity * m_direction * deltaTime;

    setAnimation(keycode, playerId);

    std::string key = m_animationFrame + std::to_string(m_currentFrame);
    auto it = m_spritesSheet->find(key);
    if (it != m_spritesSheet->end()) {
        m_currentSpriteRect = &(it->second);
    }

    m_frameTime += deltaTime;
    if (m_frameTime >= m_currentSpriteRect->spriteFrameTime) {
        if (m_currentFrame >= m_maxFrame - 1) {
            m_isAnimationPlaying = false;
            m_currentFrame = 0;
            
        }
        else {
            ++m_currentFrame;
        }
        // Reinitialisez le temps pour la prochaine frame
        m_frameTime -= m_currentSpriteRect->spriteFrameTime;
    }    

    // A changer (on n'utilise plus reverse)
    if(m_reversed) {
        if(m_position.x < m_opponent->getPosition().x) {
            m_reversed = !m_reversed;
            m_direction = -m_direction;    
        }
    } else {
        if(m_position.x > m_opponent->getPosition().x) {
            m_reversed = !m_reversed;
            m_direction = -m_direction;    
        }
    }

    m_hasCollideWithOpponent = isCollidingWithOpponent();

    bool hasHitOpponent = isHittingOpponent();

    bool hasBeenHitByOpponent = isBeingHurtByOpponent();
    if(hasBeenHitByOpponent) {
        m_animationFrame = "HitStanding-";
        m_isAnimationPlaying = true;
        m_currentFrame = 0;
        m_maxFrame = 5;
        
        decreaseHealthBy(20);
        if(m_reversed) m_position.x += 60;
        else m_position.x -= 60;
    }

    updateStageContraints(deltaTime);
}

void Fighter::updateStageContraints(float deltaTime) { 
    if( m_position.x > m_cameraPosition + m_screenWidth - m_screenBorder - (m_currentSpriteRect->atlas.width - m_currentSpriteRect->collideBox.posx - 4) * m_scale) {
        m_position.x = m_cameraPosition + m_screenWidth - m_screenBorder - (m_currentSpriteRect->atlas.width - m_currentSpriteRect->collideBox.posx - 4) * m_scale;
    }

    if( m_position.x < m_cameraPosition + m_screenBorder - (m_currentSpriteRect->collideBox.posx) * m_scale) {
        m_position.x = m_cameraPosition + m_screenBorder - (m_currentSpriteRect->collideBox.posx) * m_scale;
    }
    
    if(m_hasCollideWithOpponent) {
        BoundingBox ownrct = m_currentSpriteRect->collideBox;
        BoundingBox opprct = m_opponent->m_currentSpriteRect->collideBox;

        if(m_position.x <= m_opponent->m_position.x) { // opponent comes from left
            m_position.x = 
            (m_opponent->m_position.x 
            + ((m_opponent->m_currentSpriteRect->atlas.width - opprct.posx - opprct.width) * m_scale)) 
            - ((ownrct.posx + ownrct.width)*m_scale);
            m_opponent->m_position.x += deltaTime;
        }
        if(m_position.x >= m_opponent->m_position.x) { // opponent comes from right
            m_position.x = 
            (m_opponent->m_position.x 
            + (m_opponent->m_currentSpriteRect->collideBox.posx + m_opponent->m_currentSpriteRect->collideBox.width)*m_scale
            + (m_currentSpriteRect->collideBox.posx + m_currentSpriteRect->collideBox.width - m_currentSpriteRect->atlas.width)*m_scale);
            m_opponent->m_position.x -=  deltaTime;
        }
    }
}

void Fighter::setAnimation(std::string keycode, bool playerId) {
    if (animationMap.find(keycode) != animationMap.end()) {
        auto& animationData = animationMap[keycode][playerId];
        std::string animationName = animationData.first;
        int correspondingVelocity = animationData.second.first;
        int maxFrameCount = playerId ? animationData.second.second.first : animationData.second.second.second;

        if (m_stateMachine.isTransitionAllowed(animationName) && m_animationFrame != animationName + "-" && !m_isAnimationPlaying) {
            m_stateMachine.setCurrentState(animationName);
            m_animationFrame = animationName + "-";
            m_currentFrame = 0;
            m_maxFrame = maxFrameCount;
            m_velocity = correspondingVelocity;
            if (animationName == "Stance" || animationName == "WalkForward" || animationName == "WalkBackward")
                m_isAnimationPlaying = false;
            else
                m_isAnimationPlaying = true;
        }
    }
}

